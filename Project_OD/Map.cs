        void switchLevel(int levelnumber)
        {
            switch (levelnumber)
            {
                case 1:
                    Backlayer = lvl1_Backlayer;
                    MiddleLayer= lvl1_Middlelayer;
                    Forelayer= lvl1_Forelayer;
                    break;
                case 2:
                    break;
                default:
                    break;
            }
        }

        /// <summary>
        /// Loads the needed textures
        /// </summary>
        public void LoadTextures()
        {
            TextureManager.StoreTexture();

        }

        /// <summary>
        /// Loads the array of the map.
        /// </summary>
        /// <param name="arr">Initialize the choosen map.</param>
        public void LoadArray(int[,] arr)
        {
            for (int x = 0; x < tileMapWidth; x++)
            {
                for (int y = 0; y < tileMapHeight; y++)
                {
                    map = arr;
                }
            }
        }
        /// <summary>
        /// Draws the wished map choosen by lvlID via switch.
        /// First: Drawing the background layer.
        /// Then: Drawing the foreground layer.
        /// </summary>
        /// <param name="spriteBatch"></param>
        /// <param name="lvlID">Choses a level-design</param>
        public void DrawMap(SpriteBatch spriteBatch)
        {
            LoadArray(Backlayer);
            LoadMap(spriteBatch, TextureManager.mapTileBacklayer);
            LoadArray(MiddleLayer);
            LoadMap(spriteBatch, TextureManager.mapTileMiddlelayer);
            LoadArray(Forelayer);
            LoadMap(spriteBatch, TextureManager.mapTileForelayer);     
        }
        /// <summary>
        /// Draws the textures of the map.
        /// </summary>
        /// <param name="spriteBatch"></param>
        public void LoadMap(SpriteBatch spriteBatch, List<Texture2D> tile)
        {
            for (int x = 0; x < tileMapWidth; x++)
            {
                for (int y = 0; y < tileMapHeight; y++)
                {
                    int textureIndex = map[y, x];
                    Texture2D texture = tile[textureIndex];

                    spriteBatch.Draw(
                        texture,
                        new Rectangle(
                        x * tileWidth,
                        y * tileHeight,
                        tileWidth,
                        tileHeight),
                        Color.White);
                }
            }
        }

